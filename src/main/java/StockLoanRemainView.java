public class StockLoanRemainView {
    private String id;
    private String name;
    private int buy;
    private int sell;
    private int give;
    private int pre_balance;
    private int now_balance;
    private int limit;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getBuy() {
        return buy;
    }

    public void setBuy(int buy) {
        this.buy = buy;
    }

    public int getSell() {
        return sell;
    }

    public void setSell(int sell) {
        this.sell = sell;
    }

    public int getGive() {
        return give;
    }

    public void setGive(int give) {
        this.give = give;
    }

    public int getPre_balance() {
        return pre_balance;
    }

    public void setPre_balance(int pre_balance) {
        this.pre_balance = pre_balance;
    }

    public int getNow_balance() {
        return now_balance;
    }

    public void setNow_balance(int now_balance) {
        this.now_balance = now_balance;
    }

    public int getLimit() {
        return limit;
    }

    public void setLimit(int limit) {
        this.limit = limit;
    }
}
